class LEConv(MessagePassing):
    r"""The local extremum graph neural network operator from the
    `"ASAP: Adaptive Structure Aware Pooling for Learning Hierarchical Graph
    Representations" <https://arxiv.org/abs/1911.07979>`_ paper, which finds
    the importance of nodes with respect to their neighbors using the
    difference operator:

    .. math::
        \mathbf{x}^{\prime}_i = \mathbf{x}_i \cdot \mathbf{\Theta}_1 +
        \sum_{j \in \mathcal{N}(i)} e_{j,i} \cdot
        (\mathbf{\Theta}_2 \mathbf{x}_i - \mathbf{\Theta}_3 \mathbf{x}_j)

    where :math:`e_{j,i}` denotes the edge weight from source node :obj:`i` to
    target node :obj:`j` (default: :obj:`1`)

    Args:
        in_channels (int): Size of each input sample.
        out_channels (int): Size of each output sample.
        bias (bool, optional): If set to :obj:`False`, the layer will
            not learn an additive bias. (default: :obj:`True`).
        **kwargs (optional): Additional arguments of
            :class:`torch_geometric.nn.conv.MessagePassing`.
    """
    def __init__(self, in_channels, out_channels, bias=True, **kwargs):
        kwargs.setdefault('aggr', 'add')
        super(LEConv, self).__init__(**kwargs)

        self.in_channels = in_channels
        self.out_channels = out_channels

        self.lin1 = Linear(in_channels, out_channels, bias=bias)
        self.lin2 = Linear(in_channels, out_channels, bias=False)
        self.lin3 = Linear(in_channels, out_channels, bias=bias)

        self.reset_parameters()

    def reset_parameters(self):
        self.lin1.reset_parameters()
        self.lin2.reset_parameters()
        self.lin3.reset_parameters()

    def forward(self, x, edge_index, edge_weight=None):
        """"""
        a = self.lin1(x)
        b = self.lin2(x)

        out = self.propagate(edge_index, a=a, b=b, edge_weight=edge_weight)
        return out + self.lin3(x)

    def message(self, a_i, b_j, edge_weight):
        out = a_i - b_j
        return out if edge_weight is None else out * edge_weight.view(-1, 1)

    def __repr__(self):
        return '{}({}, {})'.format(self.__class__.__name__, self.in_channels,
                                   self.out_channels)



class CombConv_GCN(MessagePassing):

    def __init__(self, in_channels, out_channels, alpha=0., share=False, improved=False, cached=False,
                 bias=False, normalize=True, **kwargs):
        super(CombConv_GCN, self).__init__(aggr='add', **kwargs)

        self.in_channels = in_channels
        self.out_channels = out_channels
        self.improved = improved
        self.cached = cached
        self.normalize = normalize
        self.share = share
        self.alpha = torch.Tensor([alpha])
        self.intra_weight = Parameter(torch.Tensor(in_channels, out_channels))
        self.inter_weight = Parameter(torch.Tensor(in_channels, out_channels))
        self.weight = Parameter(torch.Tensor(in_channels, out_channels))

        self.reset_parameters()

    def reset_parameters(self):
        glorot(self.intra_weight)
        glorot(self.inter_weight)
        glorot(self.weight)

        self.cached_result = None
        self.cached_num_edges = None

    @staticmethod
    def norm(edge_index, num_nodes, edge_weight, edge_mask, improved=False, dtype=None):
        if edge_weight is None:
            edge_weight = torch.ones((edge_index.size(1), ), dtype=dtype, device=edge_index.device)

        fill_value = 1 if not improved else 2

        _, edge_mask = add_remaining_self_loops(edge_index, edge_mask, -1, num_nodes)
        edge_index, edge_weight = add_remaining_self_loops(edge_index, edge_weight, fill_value, num_nodes)

        row, col = edge_index
        deg = scatter_add(edge_weight, row, dim=0, dim_size=num_nodes)
        deg_inv_sqrt = deg.pow(-0.5)
        deg_inv_sqrt[deg_inv_sqrt == float('inf')] = 0

        return edge_index, deg_inv_sqrt[row] * edge_weight * deg_inv_sqrt[col], edge_mask


    def forward(self, x, edge_index, edge_weight, edge_mask):
        """"""
        if self.share:
            self.inter_weight = self.intra_weight
        x_inter = torch.matmul(x, self.inter_weight)
        x_intra = torch.matmul(x, self.intra_weight)

        if self.cached and self.cached_result is not None:
            if edge_index.size(1) != self.cached_num_edges:
                raise RuntimeError(
                    'Cached {} number of edges, but found {}. Please '
                    'disable the caching behavior of this layer by removing '
                    'the `cached=True` argument in its constructor.'.format(
                        self.cached_num_edges, edge_index.size(1)))

        if not self.cached or self.cached_result is None:
            self.cached_num_edges = edge_index.size(1)
            if self.normalize:
                    edge_index, norm, edge_mask = self.norm(edge_index, x.size(self.node_dim),
                                                            edge_weight, edge_mask, self.improved, x.dtype)
            else:
                    norm = edge_weight
            self.cached_result = edge_index, norm

        edge_index, norm = self.cached_result
        assert norm.size() == edge_mask.size()
        return self.propagate(edge_index, x_inter=x_inter, x_intra=x_intra, norm=norm, edge_mask=edge_mask) + torch.matmul(x,self.weight)


    def message(self, x_inter_j, x_intra_j, norm, edge_mask):
        '''
            edge_mask == -1 -> intra edge
            edge_mask != -1 -> inter edge
        '''
        x_inter_j *= (1 + self.alpha).to(edge_mask.device)
        x_inter_j[edge_mask == -1] = x_intra_j[edge_mask == -1]
        x_j = x_inter_j

        return norm.view(-1, 1) * x_j if norm is not None else x_j
